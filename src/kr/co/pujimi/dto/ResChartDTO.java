package kr.co.pujimi.dto;

public class ResChartDTO {
	private String res_revenue;
	private String res_grade;
	private int user_seq;
	private int res_seq;
	
	private String cp_udate;
	
	public String getRes_revenue() {
		return res_revenue;
	}
	public void setRes_revenue(String res_revenue) {
		this.res_revenue = res_revenue;
	}
	public String getRes_grade() {
		return res_grade;
	}
	public void setRes_grade(String res_grade) {
		this.res_grade = res_grade;
	}
	public int getUser_seq() {
		return user_seq;
	}
	public void setUser_seq(int user_seq) {
		this.user_seq = user_seq;
	}
	public int getRes_seq() {
		return res_seq;
	}
	public void setRes_seq(int res_seq) {
		this.res_seq = res_seq;
	}
	public String getCp_udate() {
		return cp_udate;
	}
	public void setCp_udate(String cp_udate) {
		this.cp_udate = cp_udate;
	}
	
	
}
